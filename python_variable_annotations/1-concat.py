#!/usr/bin/env python3
def concat(str1: str, str2: str) -> str:
    """
    Concatenates two strings and returns the result.

    Parameters:
    - str1 (str): The first string.
    - str2 (str): The second string.

    Returns:
    str: The concatenation of the two input strings.
    """
    return str1 + str2
